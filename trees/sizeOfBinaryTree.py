class Node:
    def __init__(self,data):
        self.left = None
        self.right = None
        self.data = data

def buildTree():
    data = int(input())
    if data == -1:
        return None
    node = Node(data)
    node.left = buildTree()
    node.right = buildTree()
    return node

def size(node):
    if node == None:
        return 0
    else:
        l = size(node.left)
        r = size(node.right)
        return l+r+1



